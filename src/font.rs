pub const FONT: [Character; 10 + 7 + 26] = {
    let mut arr = [[0; CHAR_SIZE]; NUMBERS.len() + SYMBOLS.len() + LETTERS.len()];

    let mut i = 0;
    while i < 10 {
        arr[i] = NUMBERS[i];
        i += 1;
    }
    let mut i = 0;
    while i < 7 {
        arr[i + 10] = SYMBOLS[i];
        i += 1;
    }

    let mut i = 0;
    while i < 26 {
        arr[i + 10 + 7] = LETTERS[i];
        i += 1;
    }
    arr
};
